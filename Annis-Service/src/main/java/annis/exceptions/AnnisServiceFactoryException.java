package annis.exceptions;

public class AnnisServiceFactoryException extends Exception {
	private static final long serialVersionUID = -6440920661178781203L;

	public AnnisServiceFactoryException() {
		super();
	}

	public AnnisServiceFactoryException(String message, Throwable cause) {
		super(message, cause);
	}

	public AnnisServiceFactoryException(String message) {
		super(message);
	}

	public AnnisServiceFactoryException(Throwable cause) {
		super(cause);
	}
	
}
