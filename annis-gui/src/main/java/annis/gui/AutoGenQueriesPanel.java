package annis.gui;

import annis.gui.controlpanel.CorpusListPanel;
import annis.service.objects.AnnisCorpus;
import com.vaadin.ui.Accordion;
import com.vaadin.ui.Component;
import java.util.List;

/**
 *
 * @author Benjamin Wei√üenfels <b.pixeldrama@gmail.com>
 */
public class AutoGenQueriesPanel extends Accordion
{

  private final SearchUI ui;

  private List<AnnisCorpus> allCorpora;

  public AutoGenQueriesPanel(SearchUI ui)
  {
    super();
    this.ui = ui;
  }

  @Override
  public void attach()
  {
    super.attach();
  }

  /**
   * Reads the corpora from the {@link CorpusListPanel}.
   */
  public void addCorpora(List<AnnisCorpus> allCorpora)
  {
    this.allCorpora = allCorpora;
    initExampleQueries(allCorpora);
  }

  /**
   * Puts all corpora into the accordian panel
   *
   * @param allCorpora
   */
  private void initExampleQueries(List<AnnisCorpus> allCorpora)
  {
    for (AnnisCorpus c : allCorpora)
    {
      AutoGeneratedQueries autoGenQ = new AutoGeneratedQueries(c.getName(), ui);
      addComponent(autoGenQ);
    }
  }
}
